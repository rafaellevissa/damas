public class Jogador {
	
	String Nome;
	int Cor;
	int[] NPecas;
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public int getCor() {
		return Cor;
	}
	public void setCor(int cor) {
		Cor = cor;
	}
	public int[] getNPecas() {
		return NPecas;
	}
	public void setNPecas(int[] nPecas) {
		NPecas = nPecas;
	}

}
