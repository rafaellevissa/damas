
public class Jogador {
	
	String Nome;
	int Cor;
	int[] NPeças;
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public int getCor() {
		return Cor;
	}
	public void setCor(int cor) {
		Cor = cor;
	}
	public int[] getNPeças() {
		return NPeças;
	}
	public void setNPeças(int[] nPeças) {
		NPeças = nPeças;
	}

}
